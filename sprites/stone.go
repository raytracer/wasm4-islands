package sprites

import "cart/w4"

// stone
const stoneWidth = 12
const stoneHeight = 8
const stoneFlags = 1 // BLIT_2BPP
var stone = [24]byte{0x00, 0x00, 0x00, 0x3a, 0xab, 0x00, 0x3a, 0xab, 0x00, 0xff, 0xff, 0xc0, 0xaa, 0xea, 0x80, 0xaa, 0xea, 0x80, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00}

// DrawStone draws stone at the given position
func DrawStone(x, y int) {
	*w4.DRAW_COLORS = 0x4321
	w4.Blit(&stone[0], x, y, stoneWidth, stoneHeight, stoneFlags)
}
