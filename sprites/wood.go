package sprites

import "cart/w4"

// wood
const woodWidth = 12
const woodHeight = 8
const woodFlags = 1 // BLIT_2BPP
var wood = [24]byte{0x00, 0x00, 0x00, 0x3e, 0xaa, 0xbc, 0xd7, 0xbf, 0xab, 0xd7, 0xaf, 0xfb, 0xd7, 0xaa, 0xab, 0x3e, 0xbf, 0xac, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00}

// DrawWood draws wood at the given position
func DrawWood(x, y int) {
	*w4.DRAW_COLORS = 0x4321
	w4.Blit(&wood[0], x, y, woodWidth, woodHeight, woodFlags)
}
